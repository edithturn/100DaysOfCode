# Asking the number for check it
number = int(input("Which number do you want to check? "))
# If the residue of the number divided by 2 is zero the number is Even
if number % 2 == 0:
    print("The number is Even")
# If the residue of the number divided by 2 is one the number is Odd
if number % 2 == 1:
    print("The number is Odd")
